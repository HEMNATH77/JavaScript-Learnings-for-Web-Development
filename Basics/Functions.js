// Function - Block of code designed to perform a particular task 

let a = 10
let b = 20

function add(){            // Function Creation
    c = a+b
    console.log(c)        // Function operations
}
add()                     // Function calling



// With Parameters

function add(a,b)          // function with parameters(a,b)
{
    c = a+b
    console.log(c)
}

let a = 2
let b = 5
add(a,b)                   //function calling wit parameters



// Function Return Type
// Uses "return" keyword ---> to specify the value that function should produce as a result

function movie()
{
  return "Fast and Furious"
}

const b = movie()         // calling the function 
console.log(b)             // return the value inside the movie()





